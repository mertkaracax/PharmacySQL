CREATE TABLE Customer 
( 
    TCno NUMBER(11) , 
    first_name VARCHAR2(128), 
    last_name VARCHAR2(128), 
    telephone NUMBER(10), 
    CONSTRAINT customer_TCno_pk PRIMARY KEY(TCno),
    CONSTRAINT customer_TCno_ck CHECK (TCno BETWEEN 10000000000 AND 99999999999),  
    CONSTRAINT customer_telephone_ck CHECK (telephone BETWEEN 1000000000 AND 9999999999)
);

CREATE TABLE Pharmacy 
( 
    pharmacyID NUMBER(5), 
    pharmacyName VARCHAR2(128) NOT NULL, 
    city VARCHAR2(128), 
    district VARCHAR2(128), 
    street VARCHAR2(128), 
    doorNo VARCHAR(128), 
    telephone NUMBER(10), 
    CONSTRAINT pharmacy_pharmacyID_pk PRIMARY KEY(pharmacyID),
    CONSTRAINT pharmacy_telephone_ck CHECK (telephone BETWEEN 1000000000 AND 9999999999)
);

CREATE TABLE Medicine 
( 
    medicineID NUMBER(10), 
    medicineName VARCHAR2(128) NOT NULL, 
    medicineType VARCHAR2(128), 
    dose NUMBER(10), 
    manufacturer VARCHAR2(128), 
    is_require_prescription CHAR(1) NOT NULL, 
    CONSTRAINT medicine_medicineID_pk PRIMARY KEY(medicineID),
    CONSTRAINT medicine_is_require_prescription_ck CHECK(is_require_prescription in ('T' , 'F'))
);

CREATE TABLE Stock 
( 
    pharmacyID NUMBER(5) NOT NULL, 
    medicineID NUMBER(10) NOT NULL, 
    price NUMBER(25) NOT NULL, 
    amount NUMBER(5) NOT NULL,
    CONSTRAINT stock_pk PRIMARY KEY(pharmacyID, medicineID),
    CONSTRAINT stock_pharmacyID_fk FOREIGN KEY(pharmacyID)
         REFERENCES Pharmacy(pharmacyID) 
         ON DELETE CASCADE , 
    CONSTRAINT stock_medicineID_fk FOREIGN KEY(medicineID) 
        REFERENCES Medicine(medicineID),
    CONSTRAINT stock_price_ck CHECK (price > 0),
    CONSTRAINT stock_amont_ck CHECK (amount >= 0)
);

CREATE TABLE Sale  
( 
    saleID NUMBER(5), 
    saleDate DATE,
    pharmacyID NUMBER(5) NOT NULL, 
    TCno NUMBER(11) NOT NULL, 
    CONSTRAINT sale_saleID_pk PRIMARY KEY(saleID),
    CONSTRAINT sale_pharmacID_fk FOREIGN KEY(pharmacyID) 
        REFERENCES Pharmacy(pharmacyID) 
        ON DELETE CASCADE,
    CONSTRAINT sale_TCno_fk FOREIGN KEY(TCno) REFERENCES Customer(TCno)
);

CREATE TABLE Invoice 
( 
    invoiceID NUMBER(5), 
    saleID NUMBER(5) NOT NULL,
    CONSTRAINT invoice_invoiceID_pk PRIMARY KEY(invoiceID),
    CONSTRAINT invoice_saleID_fk FOREIGN KEY(saleID) 
        REFERENCES Sale(saleID) 
        ON DELETE CASCADE
);

CREATE TABLE SoldItems 
( 
    saleID NUMBER(5) NOT NULL, 
    medicineID NUMBER(10) NOT NULL, 
    quantity NUMBER(7) NOT NULL, 
    discount NUMBER(7) DEFAULT 0 ,
    CONSTRAINT soldItems_pk PRIMARY KEY(saleID, medicineID),

    CONSTRAINT soldItems_saleID_fk FOREIGN KEY(saleID)
        REFERENCES Sale(saleID)
        ON DELETE CASCADE,

    CONSTRAINT sold_items_medicineID_fk FOREIGN KEY(medicineID)
        REFERENCES Medicine(medicineID),

    CONSTRAINT soldItems_quantity_ck check(quantity > 0 )
);

CREATE TABLE Prescription 
( 
    presID NUMBER(5), 
    doctor VARCHAR2(128), 
    TCno NUMBER(11) NOT NULL,
    CONSTRAINT prescription_presID_pk PRIMARY KEY(presID),

    CONSTRAINT prescription_fk FOREIGN KEY(TCno)
    REFERENCES Customer(TCno)
);

CREATE TABLE Contains 
( 
    presID NUMBER(5) NOT NULL, 
    medicineID NUMBER(5) NOT NULL,
    CONSTRAINT contains_pk PRIMARY KEY(presID, medicineID),

    CONSTRAINT contains_fk FOREIGN KEY(presID)
        REFERENCES Prescription(presID)
        ON DELETE CASCADE,

    CONSTRAINT contains_medicineID_fk FOREIGN KEY(medicineID) 
        REFERENCES Medicine(medicineID)
);

CREATE TABLE Employee 
( 
    employeeID NUMBER(5), 
    firstName VARCHAR2(128) NOT NULL, 
    lastName VARCHAR2(128) NOT NULL, 
    empTelephoneNumber NUMBER(10), 
    monthlySalary NUMBER(10),
    pharmacyID NUMBER(5),
    CONSTRAINT employee_employeeID_pk PRIMARY KEY(employeeID),

    CONSTRAINT employee_fk FOREIGN KEY(pharmacyID)
        REFERENCES Pharmacy(pharmacyID)
        ON DELETE CASCADE,
    
    CONSTRAINT employee_empTelephoneNumber_ck CHECK (empTelephoneNumber BETWEEN 1000000000 AND 9999999999),
    CONSTRAINT employee_monthlySalary_ck CHECK (monthlySalary >= 0 )
);

CREATE TABLE empLogin 
( 
    loginID NUMBER(5), 
    loginPassword VARCHAR2(15), 
    loginTime TIMESTAMP, 
    employeeID NUMBER(5) NOT NULL,
    CONSTRAINT login_empaloginID_pk PRIMARY KEY(loginID),

    CONSTRAINT empLogin_employeeID__fk FOREIGN KEY(employeeID)
        REFERENCES Employee(employeeID)
);




